package DTO;

/**
 *
 * @author Admin
 */
public class Category {
    private String name;
    private String ID;

    public Category() {
    }

    public Category(String ID, String name) {
        this.name = name;
        this.ID = ID;
    }   
    
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the ID
     */
    public String getID() {
        return ID;
    }

    /**
     * @param ID the ID to set
     */
    public void setID(String ID) {
        this.ID = ID;
    }
    
    @Override
    public String toString()
    {
        return ID + name;
    }    
}
